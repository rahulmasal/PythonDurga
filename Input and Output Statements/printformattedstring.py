# %i signed decimal value
# %d signed decimal value
# %f float value
# %s string

a=10
print('a value: %i' %a)

b,c,d=30,40,50
print('a=%d , b=%d , c=%d' %(b,c,d))

e=[10,20,30,40]
print('hello %s'%e)

name='durga'
print('hello %s'%name)

price=50.434
print('price value = {}'.format(price))
print('price value %.2f'%price)